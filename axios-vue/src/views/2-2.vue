        patch: 更新数据 （只将修改的数据推送到后端）
        delete:  删除数据

*/

import axios from 'axios'
export default {
  name: 'axios2-2',
  components: {
    
  },
  created(){
    // 请求路径https://localhost:8080/data.json?id=12
